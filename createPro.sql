SELECT 
  o.OrderID,
  ELT(FLOOR(1 + RAND() * 4),
      'Credit Card', 'QR PromptPay', 'Bank Transfer', 'E-Wallet') AS PaymentMethod,
  FLOOR(100000000 + RAND() * 900000000) AS TaxID,
  CONCAT(m.First_Name, ' ', m.Last_Name) AS Payee,
  SUM(eb.Price * od.Quantity) AS TotalAmount
FROM Orders o
JOIN Member m         ON o.MemberID = m.MemberID
