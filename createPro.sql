INSERT INTO Receipt (PaymentMethod, PreOrderID, TaxID, Payee, TotalAmount)
SELECT 
  ELT(FLOOR(1 + RAND() * 4),
      'Credit Card', 'QR PromptPay', 'Bank Transfer', 'Cash on Delivery') AS PaymentMethod,
  p.PreOrderID,
  FLOOR(100000000 + RAND() * 900000000) AS TaxID,
  CONCAT(m.First_Name, ' ', m.Last_Name) AS Payee,
  SUM(b.Price * pod.Quantity) AS TotalAmount
FROM Pre_Order p
JOIN Member m ON p.MemberID = m.MemberID
JOIN Pre_OrderDetail pod ON p.PreOrderID = pod.PreOrderID
JOIN Book b ON b.BookID = pod.BookID
GROUP BY p.PreOrderID;


